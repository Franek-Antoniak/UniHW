public class Ex3 {
	public static void main(String[] args) {
		// {+,-,*,/,%} on char
		System.out.println("On char:");
		System.out.println("a + b = " + (char)('a' + 'b'));
		System.out.println("a - b = " + (char)('a' - 'b'));
		System.out.println("a * b = " + (char)('a' * 'b'));
		System.out.println("a / b = " + (char)('a' / 'b'));
		System.out.println("a % b = " + (char)('a' % 'b'));
		// {+,-,*,/,%} on floats
		System.out.println("On floats: ");
		System.out.println("2.5 + 3.7 = " + (2.5f + 3.7f));
		System.out.println("2.5 - 3.7 = " + (2.5f - 3.7f));
		System.out.println("2.5 * 3.7 = " + (2.5f * 3.7f));
		System.out.println("2.5 / 3.7 = " + (2.5f / 3.7f));
		System.out.println("2.5 % 3.7 = " + (2.5f % 3.7f));
		// {+,-,*,/,%} on integers
		System.out.println("On integers: ");
		System.out.println("2 + 3 = " + (2 + 3));
		System.out.println("2 - 3 = " + (2 - 3));
		System.out.println("2 * 3 = " + (2 * 3));
		System.out.println("2 / 3 = " + (2 / 3));
		System.out.println("2 % 3 = " + (2 % 3));
	}
}
